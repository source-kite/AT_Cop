uint32_t BKDR_Hash( uint8_t *p_data, uint32_t length, uint32_t seed )
{
  uint32_t hash;

  hash = 0;
  while( length -- )
  {
    hash = hash * seed + *p_data;
    p_data ++;
  }

  return hash;
}

